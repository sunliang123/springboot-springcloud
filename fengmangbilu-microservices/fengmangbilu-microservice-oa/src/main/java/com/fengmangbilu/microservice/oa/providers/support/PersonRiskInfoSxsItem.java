package com.fengmangbilu.microservice.oa.providers.support;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class PersonRiskInfoSxsItem {

    @XmlElement
    private String recordId;
    
    @XmlElement(name = "fix")
    private PersonRiskInfoSxsItemFix fix;
    

    public String getRecordId() {
        return recordId;
    }

    public void setRecordId(String recordId) {
        this.recordId = recordId;
    }

    public PersonRiskInfoSxsItemFix getFix() {
        return fix;
    }

    public void setFix(PersonRiskInfoSxsItemFix fix) {
        this.fix = fix;
    }

}
